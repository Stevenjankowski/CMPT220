public class ASMD {
    public static void main(String[] args) {
        
        int value = 100;

        
        value += 10;
        System.out.println("After Addition: " + value); // 110

        
        value -= 5;
        System.out.println("After Subtraction: " + value); // 105

        
        value *= 2;
        System.out.println("After Multiplication: " + value); // 210

        
        value /= 4;
        System.out.println("After Division: " + value); // 52
    }
}